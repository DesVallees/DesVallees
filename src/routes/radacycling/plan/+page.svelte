    <h1>Plan de Trabajo para el Desarrollo del Sitio Web de RADA Cycling Wear</h1>
    
    <p><strong>Inicio del Proyecto:</strong> 2 de enero de 2024</p>
    <p><strong>Fecha de Entrega:</strong> 7 de marzo de 2024</p>
    
    <hr>
    
    <h2>1. Configuración del Proyecto en Firebase (2 de enero de 2024)</h2>
    <ul>
        <li><strong>Descripción:</strong> Crear un proyecto en Firebase y configurar Firestore para la base de datos y Firebase
            Authentication para el sistema de autenticación.</li>
        <li><strong>Fecha de Finalización:</strong> 2 de enero de 2024</li>
    </ul>
    
    <hr>
    
    <h2>2. Desarrollo del Front-end - Fase 1 (3 de enero - 24 de enero de 2024)</h2>
    <p><strong>Descripción:</strong> Desarrollar las páginas necesarias para permitir ventas en el sitio utilizando tecnologías web (HTML, CSS, JavaScript, Svelte, SvelteKit),
        asegurándose de que puedan ser utilizadas en cualquier dispositivo y sean visualmente atractivas.</p>
    <ul>
        <li>Página de Inicio (3-7 de enero de 2024)</li>
        <li>Página de Catálogo (8-11 de enero de 2024)</li>
        <li>Página de Producto (12-16 de enero de 2024)</li>
        <li>Página de Carrito (17-20 de enero de 2024)</li>
        <li>Página de Pago, incluyendo idea de recomendar más productos antes de pagar (21-24 de enero de 2024)</li>
    </ul>
    <p><strong>Fecha de Finalización:</strong> 24 de enero de 2024</p>

    <hr>

    <h2>3. Primer Presentación del Sitio Web para Revisión y Retroalimentación (25 de enero de 2024)</h2>
    <ul>
        <li><strong>Descripción:</strong> Mostrar el sitio web para revisión y recopilar cualquier retroalimentación o ajustes
            necesarios.</li>
        <li><strong>Fecha de Finalización:</strong> 25 de enero de 2024</li>
    </ul>
    
    <hr>
    
    <h2>4. Integración de Stripe para Pagos (25-29 de enero de 2024)</h2>
    <ul>
        <li><strong>Descripción:</strong> Configurar y conectar Stripe para habilitar pagos seguros en la página de pago.</li>
        <li><strong>Fecha de Finalización:</strong> 29 de enero de 2024</li>
    </ul>

    <hr>
    
    <h2>5. Primer Lanzamiento del Sitio Web con Funcionalidades esenciales (30 de enero de 2024)</h2>
    <ul>
        <li><strong>Descripción:</strong> Realizar las configuraciones para lanzar una primera versión del sitio web al público.</li>
        <li><strong>Fecha de Finalización:</strong> 30 de enero de 2024</li>
    </ul>
    
    <hr>

    <h2>6. Desarrollo del Front-end - Fase 2 (31 de enero - 17 de febrero de 2024)</h2>
    <p><strong>Descripción:</strong> Desarrollar las páginas del sitio utilizando tecnologías web (HTML, CSS, JavaScript, Svelte, SvelteKit),
        asegurándose de que puedan ser utilizadas en cualquier dispositivo y sean visualmente atractivas.</p>
    <ul>
        <li>Página de Contacto (31 de enero - 2 de febrero 2024)</li>
        <li>Página de Nuestro Trabajo (3-6 de febrero de 2024)</li>
        <li>Página de Inicio de Sesión (7-9 de febrero de 2024)</li>
        <li>Página de Perfil (10-13 febrero de 2024)</li>
        <li>Página de Administración de Productos (14-18 de febrero de 2024)</li>
        <li>Pop-up de Máquina Tragamonedas (19-21 de febrero de 2024)</li>
    </ul>
    <p><strong>Fecha de Finalización:</strong> 21 de febrero de 2024</p>
    
    <hr>

    <h2>7. Integración de Base de Datos y Autenticación (22-28 de febrero de 2024)</h2>
    <ul>
        <li><strong>Descripción:</strong> Incorporar el sistema de autenticación en las páginas relevantes (Inicio de
            Sesión, Perfil, Carrito, etc.) y conectar la base de datos.</li>
        <li><strong>Fecha de Finalización:</strong> 28 de febrero de 2024</li>
    </ul>
    
    <hr>

    <h2>8. Presentación del Sitio Web para Revisión y Retroalimentación (29 de febrero de 2024)</h2>
    <ul>
        <li><strong>Descripción:</strong> Mostrar el sitio web para revisión y recopilar cualquier retroalimentación o ajustes
            necesarios.</li>
        <li><strong>Fecha de Finalización:</strong> 29 de febrero de 2024</li>
    </ul>
    
    <hr>
    
    <h2>9. Implementación de Cambios (1-6 de marzo de 2024)</h2>
    <ul>
        <li><strong>Descripción:</strong> Realizar los cambios sugeridos durante la revisión.</li>
        <li><strong>Fecha de Finalización:</strong> 6 de marzo de 2024</li>
    </ul>
    
