    <h2>1. Configuración del Proyecto en Firebase (5 de enero de 2024)</h2>
    <ul>
        <li><strong>Descripción:</strong> Crear un proyecto en Firebase y configurar Firestore para la base de datos y Firebase
            Authentication para el sistema de autenticación.</li>
        <li><strong>Fecha de Finalización:</strong> 5 de enero de 2024</li>
    </ul>
    
    <hr>
    
    <h2>2. Desarrollo del Front-end (6 de enero - 17 de febrero de 2024)</h2>
    <p><strong>Descripción:</strong> Desarrollar las páginas del sitio utilizando tecnologías web (HTML, CSS, JavaScript, Svelte, SvelteKit),
        asegurándose de que puedan ser utilizadas en cualquier dispositivo y sean visualmente atractivas.</p>
    <ul>
        <li>Página de Inicio (6-10 de enero de 2024)</li>
        <li>Página de Catálogo (11-15 de enero de 2024)</li>
        <li>Página de Contacto (16-18 de enero de 2024)</li>
        <li>Página de Nuestro Trabajo (20-23 de enero de 2024)</li>
        <li>Página de Producto (24-26 de enero de 2024)</li>
        <li>Página de Inicio de Sesión (27-29 de enero de 2024)</li>
        <li>Página de Perfil (30 de enero - 2 de febrero de 2024)</li>
        <li>Página de Carrito (3-5 de febrero de 2024)</li>
        <li>Página de Pago, incluyendo idea de recomendar más productos antes de pagar (6-9 de febrero de 2024)</li>
        <li>Página de Administración de Productos (10-14 de febrero de 2024)</li>
        <li>Pop-up de Máquina Tragamonedas (15-17 de febrero de 2024)</li>
    </ul>
    <p><strong>Fecha de Finalización:</strong> 17 de febrero de 2024</p>
    
    <hr>
    
    <h2>3. Integración de Base de Datos y Autenticación (18-25 de febrero de 2024)</h2>
    <ul>
        <li><strong>Descripción:</strong> Incorporar el sistema de autenticación en las páginas relevantes (Inicio de
            Sesión, Perfil, Carrito, etc.) y conectar la base de datos.</li>
        <li><strong>Fecha de Finalización:</strong> 25 de febrero de 2024</li>
    </ul>
    
    <hr>
    
    <h2>4. Integración de Stripe para Pagos (26-28 de febrero de 2024)</h2>
    <ul>
        <li><strong>Descripción:</strong> Configurar y conectar Stripe para habilitar pagos seguros en la página de pago.</li>
        <li><strong>Fecha de Finalización:</strong> 28 de febrero de 2024</li>
    </ul>
    
    <hr>
    
    <h2>5. Presentación del Sitio Web para Revisión y Retroalimentación (29 de febrero de 2024)</h2>
    <ul>
        <li><strong>Descripción:</strong> Mostrar el sitio web para revisión y recopilar cualquier retroalimentación o ajustes
            necesarios.</li>
        <li><strong>Fecha de Finalización:</strong> 29 de febrero de 2024</li>
    </ul>
    
    <hr>
    
    <h2>6. Implementación de Cambios (1-6 de marzo de 2024)</h2>
    <ul>
        <li><strong>Descripción:</strong> Realizar los cambios sugeridos durante la revisión.</li>
        <li><strong>Fecha de Finalización:</strong> 6 de marzo de 2024</li>
    </ul>
    
    <hr>
    
    <h2>7. Capacitación (7 de marzo de 2024)</h2>
    <ul>
        <li><strong>Descripción:</strong> Brindar una sesión de capacitación sobre cómo administrar y actualizar el sitio web.</li>
        <li><strong>Fecha de Finalización:</strong> 7 de marzo de 2024</li>
    </ul>
    
